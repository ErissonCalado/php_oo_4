<div id=medio>
    <h1>Ensino Medio</h1>
    <p>Além de uma equipe de professores com larga experiência em aprovação, o Ensino Médio do Colégio HGE possui uma carga horária reforçada e aulas direcionadas aos exames do Seriado da UPE e do ENEM. Com isso, o aluno HGE realiza uma preparação muito mais eficiente, estudando de forma aprofundada todo o conteúdo abordado nestes exames.

Além disso, o Ensino Médio do HGE diferencia-se pelos recursos do Sistema HGE de Ensino, que oferece material didático de alto nível e uma plataforma interativa, com ferramentas avançadas que proporcionam, no dia a dia, o melhor aprendizado.

Ano após ano, o HGE vem sendo destaque na classificação do ENEM (Exame Nacional do Ensino Médio), sendo o colégio com maior número de aprovações em Pernambuco, o que comprova o alto nível de aprendizado e formação dos alunos. A média de aprovações obtida por cada uma das unidades do Colégio HGE ficou bem acima da nacional e da de Pernambuco, confirmando o trabalho educacional realizado pela Instituição.</p>
