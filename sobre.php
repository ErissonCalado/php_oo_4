<div id=fund2>
    <h1>Ensino Fundamental II</h1>
    <p>
    O Fundamental 2 do Colégio HGE contempla uma abordagem mais tradicional de ensino. Afirmamos o tradicional porque, apesar de utilizarmos recursos multimídia, bem como ferramentas tecnológicas para promovermos o aprendizado de nossos alunos, prezamos também pelo conteúdo, pelo ensino de qualidade, pelas aulas mais centradas na figura do professor e defendemos o estudo individual como um forte recurso para obtenção de um bom desempenho acadêmico.

Buscamos desenvolver o pensamento crítico, moral, intelectual e humanístico, visando o desenvolvimento integral de nossos alunos.

Nossa proposta pedagógica está amparada por três pilares: conteúdo, formação e acompanhamento.

Conteúdo: por sermos uma instituição de ensino formal, o trabalho com conhecimento é a nossa função primordial
Formação: pois entendemos que aprender e cumprir regras e ter disciplina para alcançar objetivos é um dos principais preceitos dos alunos que terão êxito em seu futuro
Acompanhamento: Nesta fase do aprendizado, o acompanhamento do desempenho dos alunos é diferenciado e individualizado, com uso de ferramentas pedagógicas e tecnologia que garantem o desempenho do aluno dentro e fora de sala de aula. Além disso, oferecemos projetos que aproximam a família da escola, de modo a garantir todo o suporte necessário para que o processo de ensino-aprendizado aconteça com sucesso.
    </p>

